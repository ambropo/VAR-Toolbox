function c = cols(DATA)
% =======================================================================
% Returns number of columns in a matrix
% =======================================================================
% c = cols(DATA)
% -----------------------------------------------------------------------
% INPUT
%   - DATA: input matrix (nobs X nvars)
% -----------------------------------------------------------------------
% OUTPUT
%	- r: number of columns in DATA 
% -----------------------------------------------------------------------
% EXAMPLE
%   x = rand(20,1);
%   OUT = cols(x)
% =======================================================================
% VAR Toolbox 3.1
% Ambrogio Cesa-Bianchi
% ambrogiocesabianchi@gmail.com
% March 2012. Updated March 2024
% -----------------------------------------------------------------------

[~,c] = size(DATA);
