function out = NoSpace(str)
% =======================================================================
% Removes spaces from a string
% =======================================================================
% out = NoSpace(str)
% -----------------------------------------------------------------------
% INPUT
%	- str : string
% -----------------------------------------------------------------------
% OUTPUT
%	- out : string with no spaces
% -----------------------------------------------------------------------
% EXAMPLE
%   x = 'this is a string with spaces';
%   out = NoSpace(x)
% =======================================================================
% VAR Toolbox 3.1
% Ambrogio Cesa-Bianchi
% ambrogiocesabianchi@gmail.com
% March 2012. Updated March 2024
% -----------------------------------------------------------------------

out = str(~isspace(str));